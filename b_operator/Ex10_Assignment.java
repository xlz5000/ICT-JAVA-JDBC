package b_operator;

public class Ex10_Assignment {

	public static void main(String[] args) {
		
		


		
		/*
		 *  축약대입연산자
		 *  	- 산술	+=	-=	*=	/=	%=
		 *  
		 *  
		 *  	- 이진논리	|=	&=	^=
		 *  	- 쉬프트	<<=	>>=
		 *  
		 */
		int a = 10, b = 7;
 		
 		a += b; // a = a + b 
 		System.out.println(a); // 17
 		
 		a -= b; // a = a - b
 		System.out.println(a); // 10
 		
 		a *= b; // a = a * b
 		System.out.println(a); // 70
 		
 		a %= b; // a = a % b
 		System.out.println(a); // 0
 		
 		a /= b; // a = a / b
 		System.out.println(a); //0
 		
 		
 		
 		
	}

}
